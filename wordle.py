    def guess(self, guess: str, word: str = None):
        returned_list = []
        guess = guess.lower()
        if not word:
            word = self.get_word()

        if len(guess) != 5:
            raise Exception("Guess must equal 5 characters")

        if len(word) != 5:
            raise Exception("Word must equal 5 characters")

        for idx, char in enumerate(guess):
            if word[idx] == char:
                returned_list.append(
                    f"{char} - This letter is in the word and in the right position"
                )
            elif char in word:
                returned_list.append(
                    f"{char} - This letter is in the word, but in the wrong position"
                )
            else:
                returned_list.append(f"{char} - This letter is not in the word")

        if guess == word:
            returned_list.clear()
            returned_list.append(f"{char} - This is the word! Congrats!")

        return returned_list
